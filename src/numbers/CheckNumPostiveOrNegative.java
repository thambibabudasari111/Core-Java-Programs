package numbers;

public class CheckNumPostiveOrNegative {
	
	/*
	 * To test if number is +ve or -ve
	 */

	public static void main(String[] args) {

		int num = 0; //declare and initialize num to -5
		
		if(num==0)
		{
			System.out.println("It is zero");
		}
		
		else if(num >0)
		{
			System.out.println(num+" is positive");
		}
		else
		{
			System.out.println(num+" is negative");
		}
		
	}

}
