package numbers;

public class Num1To10UsingWhile {

	public static void main(String[] args) {


		/*
		 * To display numbers from 1 to 10 using while loop
		 */
		
		int num = 1; //declare and initialize number to 1;
		
		while(num<=10)
		{
			System.out.println(num); //display number
			num++; //increment number
		}
	}

}
